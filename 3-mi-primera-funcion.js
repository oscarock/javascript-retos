// crear una función suma que sume dos números y retorne el resultado.
// Ejemplos:

// suma(1, 2) //=> 3
// suma(0, 0) //=> 0
// suma(245, 923) //=> 1168

// mi solucion
function suma(num1,num2) {
  resultado = num1 + num2;
  return resultado;
}

suma(1, 2) //=> 3
suma(0, 0) //=> 0
suma(245, 923) //=> 1168